package com.example.ticketmanagementsystem.Service;

public class TicketActions {

    private int added;
    private int bought;

    // Getter and Setter for added tickets
    public int getAdded() {
        return added;
    }

    public void setAdded(int added) {
        this.added = added;
    }

    // Getter and Setter for bought tickets
    public int getBought() {
        return bought;
    }

    public void setBought(int bought) {
        this.bought = bought;
    }
}
